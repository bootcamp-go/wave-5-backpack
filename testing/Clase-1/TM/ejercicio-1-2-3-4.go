//¿Cuáles son las diferencias entre White Box y Black Box?

/*

La principal diferencia es que la Black Box no tiene
conocimiento de la estructura, diseño o funcionamiento
interno por quien ejecuta un test, mientras que el 
White Box sí lo conoce.

 */

//Ejercicio 2
//¿Qué es un test funcional?.

/*Son pruebas que se basan en la entrada y salida
de Software, con el objetivo de comproborar que la
respuesta de software ante cada escenario
coincida con el resultado esperado. */

//Ejercicio 3
//¿Qué es un Test de Integración?

/* Son los que prueban la comunicación entre distintos
componentes o capas de la aplicación. El objetivo es 
comprobar que todos aquellos bloques de código que 
fueron probados de forma unitaria, interactúen y se 
cominiquen entre sí generando los resultados esperados*/


